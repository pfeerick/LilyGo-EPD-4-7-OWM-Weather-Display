import os
import subprocess
from datetime import datetime, timezone
Import("env")

project_dir = env.subst("$PROJECT_DIR")

def git(args):
    try:
        return subprocess.check_output(["git"] + args, cwd=project_dir, stderr=subprocess.DEVNULL).decode().strip()
    except Exception:
        return None

sha    = git(["rev-parse", "--short", "HEAD"]) or "unknown"
branch = git(["rev-parse", "--abbrev-ref", "HEAD"]) or "unknown"
dirty  = git(["status", "--porcelain"]) != ""  # non-empty means uncommitted changes
build_time = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
build_info = f"{branch}@{sha}{'*' if dirty else ''} &bull; {build_time}"

files = [
    ("web/config.html", "include/config_html.h", "CONFIG_HTML"),
    ("web/update.html", "include/update_html.h", "UPDATE_HTML"),
]

for html_rel, header_rel, varname in files:
    html_path   = os.path.join(project_dir, html_rel)
    header_path = os.path.join(project_dir, header_rel)

    with open(html_path, "r", encoding="utf-8") as f:
        html = f.read()

    html = html.replace("__BUILD__", build_info)

    new_content = (
        "// Auto-generated by scripts/embed_html.py — do not edit\n"
        "#pragma once\n\n"
        f'static const char {varname}[] PROGMEM = R"rawhtml(\n'
        + html
        + '\n)rawhtml";\n'
    )

    try:
        with open(header_path, "r", encoding="utf-8") as f:
            existing = f.read()
    except FileNotFoundError:
        existing = None

    if existing != new_content:
        with open(header_path, "w", encoding="utf-8") as f:
            f.write(new_content)
        print(f"Regenerated {header_rel} from {html_rel}")
